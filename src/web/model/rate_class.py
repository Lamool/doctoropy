class rate_poke_mon_A :
    def __init__(self, hp, atk, _def, spe_atk, spe_def, speed, type, sk_dam):
        self.hp = int(hp)
        self.atk = int(atk)
        self._def = int(_def)
        self.spe_atk = int(spe_atk)
        self.spe_def = int(spe_def)
        self.speed = int(speed)
        self.sk_dam = int(sk_dam)
        self.type = type



